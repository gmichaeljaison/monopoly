    private void joinGame(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_joinGame
        // TODO add your handling code here:
        for (Player player : Game.getPlayers()) {
            if (player.getName().equals(name.getText())) {
                JOptionPane.showMessageDialog(this, "Player name cannot be duplicated");
                return;
            }
            if (player.getColor().equals(color_)) {
                JOptionPane.showMessageDialog(this, "Player color cannot be duplicated");
                return;
            }
        }
        this.dispose();
        Game.gameUI = new GameUI();
        Game.thread.start();
        Game.thread.sendMessage("p|"+name.getText()+"|"+String.valueOf(color_.getRGB()));        
    }//GEN-LAST:event_joinGame

