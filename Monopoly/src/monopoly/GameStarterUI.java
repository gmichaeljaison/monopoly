    @Override
    public void paint(Graphics g) {

        
        int rowGap = 15, colGap = 20;
        int y = startY+rowGap;
        for (Player player : Game.getPlayers()) {
            g.setColor(Color.BLACK);
            g.drawString(player.getName(), startX, y);
            g.setColor(player.getColor());
            g.fillRect(startX+colGap, y, 100, 20);            
            y += rowGap;
        }
    }
    
